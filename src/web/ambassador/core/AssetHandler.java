    /**
     * Retrieves an asset from the classpath based on the provided path and returns
     * the asset content along with its MIME type.
     * <p>
     * The method strips the `/assets/` prefix from the path, sanitizes the path
     * to avoid directory traversal attacks, and attempts to locate the asset
     * from the classpath.
     * </p>
     *
     * @param path The path to the asset (must start with "/assets/").
     * @return An {@code AssetResponse} containing the asset content, MIME type,
     *         and whether the asset was found and successfully loaded.
     */
    public AssetResponse getAsset(String path) {
        path = path.replaceFirst("^/assets/", "");
        path = "assets/" + path.replace("..", "");

        try {
            InputStream inputStream = getClass().getClassLoader().getResourceAsStream(path);
            if(inputStream == null)
                return new AssetResponse(null, null, false);

            byte[] content = inputStream.readAllBytes();
            inputStream.close();

            String extension = "";
            int i = path.lastIndexOf('.');
            if(i > 0)
                extension = path.substring(i + 1).toLowerCase();

            String mimeType = AssetHandler.mimeTypes.getOrDefault(
                extension,
                "application/octet-stream"
            );
            return new AssetResponse(
                content,
                mimeType,
                true
            );
        }
        catch(IOException e) {
            return new AssetResponse(null, null,false);
        }
    }

