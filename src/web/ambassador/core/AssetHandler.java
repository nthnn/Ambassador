/**
 * The AssetHandler class is responsible for handling the retrieval of static assets
 * (e.g., images, CSS files, JavaScript files) from the classpath and returning them
 * along with the appropriate MIME type for proper content handling in web responses.
 *
 * This class contains the logic for:
 * <ul>
 *     <li>Fetching the asset's content from the classpath using the provided path.</li>
 *     <li>Determining the asset's MIME type based on its file extension.</li>
 *     <li>Returning an AssetResponse containing the asset content and MIME type.</li>
 * </ul>
 *
 * The class also includes a static map of known file extensions to MIME types, which helps in setting the correct content type for various asset files.
 *
 * <p><b>Usage:</b></p>
 * ```java
 *     AssetHandler assetHandler = new AssetHandler();
 *     AssetResponse response = assetHandler.getAsset("/assets/image.png");
 *
 *     if(response.wasFound()) {
 *         // Process the content and MIME type
 *     }
 * ```
 *
 * @author  <a href="https://github.com/nthnn" target="_blank">Nathanne Isip</a> &lt;<a href="mailto:nathanneisip@gmail.com">nathanneisip@gmail.com</a>&gt;
 * @since 1.0
 * @version 1.0
 * @see web.ambassador.core.AssetResponse
 */
